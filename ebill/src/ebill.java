    private void txtcnameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcnameKeyPressed
       
       
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c) || Character.isWhitespace(c) || Character.isISOControl(c))
        {
            
            txtcname.setEditable(true);
        }
        else
        {
            getToolkit().beep();
            txtcname.setEditable(false);
        }
        
        
    }//GEN-LAST:event_txtcnameKeyPressed

    private void txtreset1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtreset1ActionPerformed
       
       Connection conn;
        PreparedStatement stm;
        try{
            String url="jdbc:mysql://localhost:3306/ebill";
            String user="root";
            String pass="root";
            
            conn=DriverManager.getConnection(url,user,pass);
            
            String id = txtcid.getText();
            String name = txtcname.getText();
            String unit = txtunit.getText();
            double amount = grandtotal;
           String sql="INSERT INTO users VALUES (?,?,?,?)";
           
           stm=conn.prepareStatement(sql);
           
           stm.setString(1, id);
           stm.setString(2, name);
           stm.setString(3, unit);
           stm.setDouble(4, amount);
         
           if(stm.executeUpdate()>0){
           JOptionPane.showMessageDialog(this, "Record Inserted");
           } 
           else{
               JOptionPane.showMessageDialog(this, "Something went wrong");
           }
        }catch(Exception ex){
            System.out.println("Error is "+ex.getMessage());
        }
        
        
        
        
        
    }//GEN-LAST:event_txtreset1ActionPerformed

