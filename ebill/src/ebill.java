      public int cal(double charge, double count)
      {
        
        count = Integer.parseInt(txtunit.getText());
        
            if(count<500)
        {
            charge = 1.00;
        }
        else if(count<500 && count<600)
        {
            charge = 1.80;
        }
        if(count<600 && count<800)
        {
            charge = 2.80;
        }
        else
        {
            charge = 3.00;
        }
        
        grandtotal = charge * count;
        
          System.out.println(grandtotal);
        
        return 0;
      }
      
      
      
      
      
    
